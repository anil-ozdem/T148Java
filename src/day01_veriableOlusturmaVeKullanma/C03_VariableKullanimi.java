package day01_veriableOlusturmaVeKullanma;

public class C03_VariableKullanimi {

    public static void main(String[] args) {

        // kullanicinin hesap bakiyesini tutan bir variable olusturun

        double bakiye = 0;

        // kullanicinin hesap bakiyesini 100 artirin

        bakiye = bakiye + 100 ;



        System.out.println(" 17.satirda bakiye : " + bakiye );

        // bakiye miktarini 3 katina cikarin

        bakiye = bakiye * 3 ;



        System.out.println(" 25.satirda bakiye : " + bakiye );




    }
}
