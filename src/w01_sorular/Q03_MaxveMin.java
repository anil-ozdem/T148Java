public class Q03_MaxveMin {

    public static void main(String[] args) {

        // Q03 -byte, short, integer,long, double, float veri tiplerinin max ve min degerlerini yazdiriniz.

        System.out.println("Byte min deger: " + Byte.MIN_VALUE + ", " + "Byte max deger: " + Byte.MAX_VALUE);
        System.out.println("Short min deger: " + Short.MIN_VALUE + ", " + "Short max deger: " + Short.MAX_VALUE);
        System.out.println("Integer min deger: " + Integer.MIN_VALUE + ", " + "Integer max deger: " + Integer.MAX_VALUE);
        System.out.println("Long min deger: " + Long.MIN_VALUE + ", " + "Long max deger: " + Long.MAX_VALUE);
        System.out.println("Double min deger: " + Double.MIN_VALUE + ", " + "Double max deger: " + Double.MAX_VALUE);
        System.out.println("Float min deger: " + Float.MIN_VALUE + ", " + "Float max deger: " + Float.MAX_VALUE);

    }
}
